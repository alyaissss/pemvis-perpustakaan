protected void autonumber() {
    try {
            String sql = "SELECT id_kembali FROM pengembalian order by id_kembali asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txtidkembali.setText("MK0001");
            while(rs.next()){
                String id_kembali = rs.getString("id_kembali").substring(2);
                int AN = Integer.parseInt(id_kembali)+1;
                String Nol = "";

                if(AN<10)
                {Nol = "000";}
                else if(AN<100)
                {Nol = "00";}
                else if(AN<1000)
                {Nol = "0";}
                else if(AN<10000)
                {Nol = "";}

                txtidkembali.setText("MK" + Nol + AN);
            }
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Auto Number Gagal" +e);
        }
}
