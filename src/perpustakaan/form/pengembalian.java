    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        String tglpinjam = sdf.format(jtgl.getValue());
        String sql = "insert into pengembalian values (?, ?, ?, ?, ?)";
       
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtidkembali.getText());
            stat.setString(2, tglpinjam);
            stat.setString(3, denda.getText());
            stat.setString(4, txtidnota.getText());
            stat.setString(5, labelID.getText());

            stat.executeUpdate();

            int t = tbltransaksi2.getRowCount();
            for (int i = 0; i < tbltransaksi2.getRowCount(); i++) {

                String kode = tbltransaksi2.getValueAt(i, 0).toString();
            }
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            cetak();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan" + e);
        }
        kosong();
        aktif();
        autonumber();
    }//GEN-LAST:event_bsimpanActionPerformed

