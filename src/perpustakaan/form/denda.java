    public void tampilBukuTerlambat() {
        Object[] Baris = {"Kode Buku", "Judul Buku", "Penulis", "Penerbit", "Kategori"};
        tabmode = new DefaultTableModel(null, Baris);

        try {  
            String sql =
                    "SELECT b.id_buku,b.judul,b.penulis,b.penerbit,b.kategori " +
                    "FROM pengembalian p " +
                    "JOIN isi i ON p.id_nota=i.id_nota " +
                    "JOIN buku b ON i.id_buku=b.id_buku " +
                    "WHERE p.id_kembali='" + txtidkembali.getText() + "'";
            
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql);

            while (rs.next()) {
                tabmode.addRow(new Object[]{
                    rs.getString("id_buku"),
                    rs.getString("judul"),
                    rs.getString("penulis"),
                    rs.getString("penerbit"),
                    rs.getString("kategori"),
                });
            }
            tblpembayaran.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal memanggil data buku: " + e);
        }
    }
    
