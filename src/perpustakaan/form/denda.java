protected void autonumber() {
        try {
            String sql = "SELECT id_denda FROM denda order by id_denda asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txtiddenda2.setText("PAY0001");
            while(rs.next()){
                String id_denda = rs.getString("id_denda").substring(3);
                int AN = Integer.parseInt(id_denda) + 1;
                String Nol = "";

                if(AN < 10) { Nol = "000"; }
                else if(AN < 100) { Nol = "00"; }
                else if(AN < 1000) { Nol = "0"; }
                else if(AN < 10000) { Nol = ""; }

                txtiddenda2.setText("PAY" + Nol + AN);
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Auto Number Gagal: " + e);
        }
    }

